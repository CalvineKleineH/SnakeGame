from turtle import Turtle
ALIGNMENT = "center"
FONT = ("Comic Sans MS", 18, "normal")


class Scoreboard(Turtle):

    def __init__(self):
        super().__init__()
        self.score = 0
        self.color("white")
        self.penup()
        self.goto(0, 270)
        self.hideturtle()
        self.score_display()

    def score_display(self):
        self.write(f"Score: {self.score}", align=ALIGNMENT, font=FONT)

    def game_over(self):
        self.goto(0, 0)
        self.write("GAME OVER", align=ALIGNMENT, font=FONT)

    def scored(self):
        self.score += 1
        self.clear()
        self.score_display()

